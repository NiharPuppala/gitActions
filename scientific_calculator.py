import math

def sine_method(val):
    return math.sin(math.radians(val))
def cosine_method(val):
    return math.cos(math.radians(val))
def tan_method(val):
    return math.tan(math.radians(val))
def square_method(val):
    return math.sqrt(val)
def logarithm_method(val):
    return math.log(val)
def exponent_method(val):
    return math.exp(val)


cal=sine_method(90)
print(cal)
